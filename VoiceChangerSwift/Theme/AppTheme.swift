import Foundation
import UIKit

extension UIColor {
    
    struct AppTheme {
        static var darkGreen: UIColor  {
            return UIColor(red: 0.128, green: 0.175, blue: 0.191, alpha: 1)
        }
        
        static var lightGreen: UIColor {
            return UIColor(red: 0.428, green: 0.475, blue: 0.491, alpha: 1)
        }
        
        static var brightGreen: UIColor {
            return UIColor(red: 0.097, green: 0.65, blue: 0.4, alpha: 1)
        }
    }
}
